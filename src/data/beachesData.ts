export const getBeachesByLocation = (location: string): Beach[] => {
  return beachesData.filter(beach => 
    beach.location.toLowerCase().includes(location.toLowerCase())
  );
};

export const getAllTags = (): string[] => {
  const allTags = beachesData.flatMap(beach => beach.tags);
  return Array.from(new Set(allTags)).sort();
};

export const getAllLocations = (): string[] => {
  const allLocations = beachesData.map(beach => beach.location);
  return Array.from(new Set(allLocations)).sort();
};

export const getTopRatedBeaches = (limit: number = 5): Beach[] => {
  return beachesData
    .sort((a, b) => b.rating - a.rating)
    .slice(0, limit);
};

export const searchBeaches = (query: string): Beach[] => {
  const searchTerm = query.toLowerCase();
  return beachesData.filter(beach =>
    beach.title.toLowerCase().includes(searchTerm) ||
    beach.description.toLowerCase().includes(searchTerm) ||
    beach.location.toLowerCase().includes(searchTerm) ||
    beach.tags.some(tag => tag.toLowerCase().includes(searchTerm))
  );
};